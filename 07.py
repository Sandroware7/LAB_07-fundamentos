# Suma de pares e impares de n numeros:

Nnumeros = int(input("Cuantos numeros quieres sumar: "))

sumaimpar = 0
sumapar = 0
conteo = 0

while conteo < Nnumeros:
    numero = int(input("ingresa un numero: "))
    conteo += 1
    if numero % 2 == 0:
        sumapar += numero
    else:
        sumaimpar += numero

print(f"La suma de pares es: {sumapar}, y la suma de impares es: {sumaimpar}")
